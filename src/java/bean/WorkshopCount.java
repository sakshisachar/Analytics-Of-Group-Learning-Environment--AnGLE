package bean;

/**
 *
 * @author Abinaya
 */
public class WorkshopCount {

    private int totalWorkshops;
    private int coordinatorWorkshops;
    private int mainWorkshops;
    private int SSMWorkshops;

    /**
     * @return the totalWorkshops
     */
    public int getTotalWorkshops() {
        return totalWorkshops;
    }

    /**
     * @param totalWorkshops the totalWorkshops to set
     */
    public void setTotalWorkshops(int totalWorkshops) {
        this.totalWorkshops = totalWorkshops;
    }

    /**
     * @return the coordinatorWorkshops
     */
    public int getCoordinatorWorkshops() {
        return coordinatorWorkshops;
    }

    /**
     * @param coordinatorWorkshops the coordinatorWorkshops to set
     */
    public void setCoordinatorWorkshops(int coordinatorWorkshops) {
        this.coordinatorWorkshops = coordinatorWorkshops;
    }

    /**
     * @return the mainWorkshops
     */
    public int getMainWorkshops() {
        return mainWorkshops;
    }

    /**
     * @param mainWorkshops the mainWorkshops to set
     */
    public void setMainWorkshops(int mainWorkshops) {
        this.mainWorkshops = mainWorkshops;
    }

    /**
     * @return the SSMWorkshops
     */
    public int getSSMWorkshops() {
        return SSMWorkshops;
    }

    /**
     * @param SSMWorkshops the SSMWorkshops to set
     */
    public void setSSMWorkshops(int SSMWorkshops) {
        this.SSMWorkshops = SSMWorkshops;
    }
}
